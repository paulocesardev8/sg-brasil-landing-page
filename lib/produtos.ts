export function formatarPreco(preco: number | null): string {
  if (preco === null) return "Sob consulta";
  return preco.toLocaleString("pt-BR", { style: "currency", currency: "BRL" });
}

/**
 * Retorna todas as mídias do produto: imagem principal + fotos extras + vídeo (se houver).
 */
export function midiasDoProduto(produto: Produto): Array<{ tipo: "imagem" | "video"; src: string }> {
  const midias: Array<{ tipo: "imagem" | "video"; src: string }> = [];
  midias.push({ tipo: "imagem", src: produto.imagem });
  if (produto.imagensExtras) {
    for (const img of produto.imagensExtras) {
      midias.push({ tipo: "imagem", src: img });
    }
  }
  if (produto.video) {
    midias.push({ tipo: "video", src: produto.video });
  }
  return midias;
}

/**
 * Agrupa produtos por nome (modelo) mantendo a ordem em que aparecem no array.
 */
export function agruparPorModelo(lista: Produto[]): Array<{ nome: string; colecao: string; itens: Produto[] }> {
  const grupos: Array<{ nome: string; colecao: string; itens: Produto[] }> = [];
  const indice = new Map<string, number>();
  for (const p of lista) {
    if (!indice.has(p.nome)) {
      indice.set(p.nome, grupos.length);
      grupos.push({ nome: p.nome, colecao: p.colecao, itens: [p] });
    } else {
      grupos[indice.get(p.nome)!].itens.push(p);
    }
  }
  return grupos;
}
